def parse_zone(zone_str):
    """Parse zone string like 'Mild [-0.5, 0.0]' → (lo, hi)."""
    import re
    nums = re.findall(r"[-+]?\d*\.?\d+|[-+]?inf", zone_str.replace("<=","").replace(">=",""))
    if len(nums) >= 2:
        def parse_num(s):
            if "inf" in s.lower():
                return -np.inf if s.startswith("-") else np.inf
            return float(s)
        return parse_num(nums[0]), parse_num(nums[1])
    return -0.5, 0.0

def parse_max_hold(max_hold_str):
    """Parse '3d' → 3 * BARS_PER_DAY."""
    try:
        days = int(max_hold_str.replace("d","").strip())
        return days * BARS_PER_DAY
    except Exception:
        return 3 * BARS_PER_DAY

def get_macro_features_at_date(ts):
    """Get macro features at a given timestamp (point-in-time)."""
    d = ts.normalize() if hasattr(ts,"normalize") else pd.Timestamp(ts,tz="UTC").normalize()
    fred_idx = fred_df.index.get_indexer([d], method="ffill")[0]
    if fred_idx < 0: fred_idx = 0

    vix_level = float(fred_df["VIXCLS"].iloc[fred_idx]) if fred_idx>=0 else 20.0
    vix_ts    = float(fred_df.get("vix_term_structure", pd.Series(1.0)).iloc[
                      min(fred_idx, len(fred_df)-1)]) if fred_idx>=0 else 1.0

    # HYG (from stock_data)
    hyg_spread = 80.0
    if "HYG" in stock_data:
        hyg_df = stock_data["HYG"]
        hyg_idx = hyg_df.index.get_indexer([ts], method="ffill")[0]
        if hyg_idx >= 0: hyg_spread = float(hyg_df["close"].iloc[hyg_idx])

    # SPY trend ratio (20d MA / 200d MA)
    spy_trend_ratio = 1.0
    if "SPY" in stock_data:
        spy_df  = stock_data["SPY"]
        spy_idx = spy_df.index.get_indexer([ts], method="ffill")[0]
        if spy_idx >= 200:
            c_s = spy_df["close"].iloc[:spy_idx]
            ma20  = c_s.iloc[-20:].mean() if len(c_s)>=20 else c_s.mean()
            ma200 = c_s.iloc[-200:].mean() if len(c_s)>=200 else c_s.mean()
            if ma200 > 0: spy_trend_ratio = float(ma20/ma200)

    # Cross-sectional dispersion (std of 1d returns across 50 assets at ts)
    cs_disp = 0.01
    rvol    = 0.15
    try:
        ts_utc = ts.tz_localize("UTC") if ts.tzinfo is None else ts
        window = 20*BARS_PER_DAY
        rets_list = []
        for tkr, df_h in stock_data.items():
            if tkr not in TRADING_UNIVERSE: continue
            idx_ = df_h.index.get_indexer([ts_utc], method="nearest")[0]
            if idx_ > 0:
                ret_ = float((df_h["close"].iloc[idx_] - df_h["close"].iloc[idx_-1])
                             / max(df_h["close"].iloc[idx_-1], 1e-9))
                rets_list.append(ret_)
        if len(rets_list) > 5:
            cs_disp = float(np.std(rets_list))
        # SPY realized vol
        if "SPY" in stock_data:
            spy_df  = stock_data["SPY"]
            spy_idx = spy_df.index.get_indexer([ts_utc], method="ffill")[0]
            if spy_idx >= window:
                spy_rets = spy_df["close"].iloc[spy_idx-window:spy_idx].pct_change().dropna()
                rvol = float(spy_rets.std() * np.sqrt(252*BARS_PER_DAY))
    except Exception:
        pass

    return {
        "vix_level": vix_level, "vix_term_structure": vix_ts,
        "hyg_spread": hyg_spread, "spy_trend_ratio": spy_trend_ratio,
        "cross_sectional_dispersion": cs_disp, "realized_vol_20d": rvol,
    }


