def check_and_clean(df, ticker, min_bars=500):
    if len(df) < min_bars: return df, False
    df = df[~df.index.duplicated(keep="last")].sort_index()
    bad = (df[["open","high","low","close"]] <= 0).any(axis=1)
    df = df[~bad]
    zero_vol = df["volume"] <= 0
    if zero_vol.sum() > 0:
        df["volume"] = df["volume"].replace(0, np.nan).ffill().bfill().fillna(1.0)
    return df, len(df) >= min_bars

def resample_to_daily(hourly_dict):
    daily = {}
    for t, df in hourly_dict.items():
        d = df.resample("1D").agg(
            open=("open","first"), high=("high","max"),
            low=("low","min"), close=("close","last"), volume=("volume","sum")
        ).dropna(subset=["close"])
        d = d[d["volume"] > 0]
        daily[t] = d
    return daily

