# CELL 19 — OOS Inference Pipeline (bar-by-bar)
# ═══════════════════════════════════════════════════════════════════════════════

def run_oos_inference(
    oos_df,
    models,
    scalers,
    hawkes_params,
    hawkes_thresholds,
    fold_id,
):
    """
    Step through OOS trades (from pre-built oos_df), score with XGBoost,
    apply Hawkes gating, select top-K per bar, simulate portfolio.
    """
    print(f"\n[OOS Inference] Fold {fold_id}  ({len(oos_df)} candidate trades)")

    if oos_df.empty:
        print("  ⚠  Empty OOS dataframe")
        return pd.DataFrame(), {}

    # Re-init Hawkes for OOS
    hawkes_procs_oos = {r: HawkesProcess(**hawkes_params[r]) for r in range(5)}

    # Score every row
    scored_rows = []
    hawkes_filtered = 0
    conf_filtered   = 0

    for regime in range(N_REGIMES):
        sub = oos_df[oos_df["gnn_regime_raw"] == regime].copy()
        if sub.empty: continue

        # Low confidence → use regime-2 model
        low_conf_mask = sub["gnn_confidence"] < MIN_REGIME_CONFIDENCE
        conf_filtered += low_conf_mask.sum()

        model  = models.get(regime)
        sc     = scalers.get(regime, scalers.get("global"))

        for use_model_key in [regime, "global"]:
            mask = (low_conf_mask == False) if use_model_key == regime else low_conf_mask
            subset = sub[mask]
            if subset.empty: continue
            use_model = models.get(use_model_key, models.get("global"))
            use_sc    = scalers.get(use_model_key, scalers.get("global"))
            if use_model is None or use_sc is None: continue

            X = subset[FEATURE_COLS].copy()
            X = X.replace([np.inf,-np.inf], np.nan).fillna(X.median()).clip(-10,10)
            X_sc = np.clip(use_sc.transform(X.values), -5, 5)

            try:
                scores = use_model.predict(X_sc)
            except Exception as e:
                print(f"  ⚠  Predict failed regime {regime}: {e}")
                scores = np.zeros(len(X_sc))

            subset = subset.copy()
            subset["xgb_score"] = scores
            scored_rows.append(subset)

    if not scored_rows:
        print("  ⚠  No scored rows")
        return pd.DataFrame(), {}

    scored_df = pd.concat(scored_rows, ignore_index=True)
    scored_df = scored_df.sort_values("entry_ts").reset_index(drop=True)

    # ── Bar-by-bar Hawkes gating + top-K selection ───────────────────────────
    scored_df["entry_day"] = pd.to_datetime(scored_df["entry_ts"]).dt.normalize()

    selected_trades = []
    hawkes_bar_filtered = 0

    # Group by entry_day (daily selection — bars within a day pooled)
    for day, day_df in scored_df.groupby("entry_day"):
        regime_today = int(day_df["gnn_regime_raw"].iloc[0])
        threshold    = hawkes_thresholds.get(regime_today, 1.0)
        hp           = hawkes_procs_oos[regime_today]
        # Use day index as time proxy
        t_proxy      = float(day_df.index[0])
        intensity    = hp.intensity(t_proxy)

        if intensity > threshold:
            hawkes_bar_filtered += len(day_df)
            continue  # Skip entire day's signals — market too excited

        # Rank by xgb_score
        ranked = day_df.sort_values("xgb_score", ascending=False)

        # Top-K by regime
        k = TOP_K_SIGNALS_BY_REGIME.get(regime_today, 4)

        # Diversity filters
        selected_today = []
        asset_count: Dict[str, int] = {}
        long_count, short_count = 0, 0

        for _, row in ranked.iterrows():
            if len(selected_today) >= k: break
            ticker_r    = row["ticker"]
            direction_r = row["direction"]
            ac = asset_count.get(ticker_r, 0)
            if ac >= MAX_CONFIGS_PER_ASSET: continue
            # Long/short balance
            if direction_r == "LONG":
                ratio = (long_count+1) / max(long_count+short_count+1, 1)
                if ratio > MAX_LONG_SHORT_RATIO and short_count > 0: continue
            asset_count[ticker_r] = ac + 1
            if direction_r == "LONG":  long_count  += 1
            else:                       short_count += 1
            selected_today.append(row)

        for row in selected_today:
            selected_trades.append(row)

        # Update Hawkes
        if selected_today:
            hp.update(t_proxy)

    print(f"  Total candidate:    {len(scored_df):,}")
    print(f"  Hawkes filtered:    {hawkes_bar_filtered:,}  "
          f"({100*hawkes_bar_filtered/max(len(scored_df),1):.1f}%)")
    print(f"  Conf filtered:      {conf_filtered:,}")
    print(f"  Selected:           {len(selected_trades):,}")

    if not selected_trades:
        print("  ⚠  No trades selected in OOS")
        return pd.DataFrame(), {
            "hawkes_filtered_pct": 100*hawkes_bar_filtered/max(len(scored_df),1),
            "conf_filtered": conf_filtered,
            "n_selected": 0
        }

    selected_df = pd.DataFrame(selected_trades).reset_index(drop=True)

    # ── Position sizing ───────────────────────────────────────────────────────
    def size_trade(row):
        regime_r    = int(row["gnn_regime_raw"])
        reg_mult    = REGIME_MULTIPLIERS.get(regime_r, 0.7)
        kelly_f     = KELLY_FRACTIONS.get(regime_r, 0.15)
        conf        = float(row["gnn_confidence"])
        score       = float(row["xgb_score"])
        # Normalise XGB score to [0,1] within day (use rank percentile)
        score_pct   = float(
            percentileofscore(scored_df["xgb_score"].values, score) / 100.0)
        base_risk   = MAX_RISK_PER_TRADE * reg_mult
        atr_e       = max(float(row["atr_entry"]), 1e-9)
        pos_size    = (base_risk * kelly_f * conf * score_pct) / atr_e
        # Dollar value
        pnl_usd     = float(row["pnl_pts"]) * pos_size
        cost_usd    = float(row["atr_entry"]) * COST_PCT_RT * pos_size
        risk_usd    = atr_e * SL_ATR_CAP * pos_size
        risk_usd    = np.clip(risk_usd, MIN_TRADE_USD, MAX_TRADE_USD)
        return pd.Series({
            "pos_size":     pos_size,
            "pnl_usd":      pnl_usd,
            "cost_usd":     cost_usd,
            "risk_usd":     risk_usd,
            "score_pct":    score_pct,
        })

    sizing = selected_df.apply(size_trade, axis=1)
    selected_df = pd.concat([selected_df, sizing], axis=1)

    meta = {
        "hawkes_filtered_pct": 100*hawkes_bar_filtered/max(len(scored_df),1),
        "conf_filtered":       conf_filtered,
        "n_candidate":         len(scored_df),
        "n_selected":          len(selected_trades),
    }
    return selected_df, meta


