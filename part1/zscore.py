def attach_atr(df_1h, period=ATR_LOOKBACK):
    daily = df_1h.resample("1D").agg(
        high=("high","max"), low=("low","min"), close=("close","last")).dropna()
    pc  = daily["close"].shift(1)
    tr  = pd.concat([daily["high"]-daily["low"],
                     (daily["high"]-pc).abs(),
                     (daily["low"]-pc).abs()], axis=1).max(axis=1)
    atr = tr.ewm(alpha=1.0/period, adjust=False).mean().shift(1).ffill()
    atr.index = atr.index.normalize()
    out = df_1h.copy()
    out["__d__"] = out.index.normalize()
    out["atr"]   = out["__d__"].map(atr)
    out.drop(columns=["__d__"], inplace=True)
    return out

def _compute_profile_vec(closes, volumes):
    if len(closes) == 0: return None
    tv = volumes.sum()
    if tv <= 0: return None
    tick = 0.01
    px   = np.round(closes/tick).astype(np.int64)
    lo, hi = px.min(), px.max(); bins = hi-lo+1
    vol_at = np.zeros(bins, dtype=np.float64)
    np.add.at(vol_at, px-lo, volumes)
    poc_idx = int(np.argmax(vol_at))
    poc = float((poc_idx+lo)*tick)
    target = tv*VA_PCT; cum = vol_at[poc_idx]
    lo_i = poc_idx; hi_i = poc_idx
    while cum < target:
        can_lo = lo_i > 0; can_hi = hi_i < bins-1
        if not can_lo and not can_hi: break
        add_lo = vol_at[lo_i-1] if can_lo else -1.0
        add_hi = vol_at[hi_i+1] if can_hi else -1.0
        if add_lo >= add_hi: lo_i -= 1; cum += vol_at[lo_i]
        else:                hi_i += 1; cum += vol_at[hi_i]
    return float((lo_i+lo)*tick), float((hi_i+lo)*tick), poc

def precompute_profiles(df):
    et_idx  = df.index.tz_convert(EASTERN)
    dates   = et_idx.normalize()
    closes  = df["close"].values.astype(np.float64)
    volumes = df["volume"].values.astype(np.float64)
    n = len(df)
    val_arr = np.full(n, np.nan); vah_arr = np.full(n, np.nan)
    completed = {}; tick = 0.01
    for day in dates.unique():
        mask = (dates==day); day_idxs = np.where(mask)[0]
        if len(day_idxs) == 0: continue
        p = _compute_profile_vec(closes[day_idxs], volumes[day_idxs])
        if p is not None: completed[pd.Timestamp(day)] = (p[0], p[1])
        nb = len(day_idxs)
        lo_px_ = np.round(closes[day_idxs]/tick).astype(np.int64).min()
        hi_px_ = np.round(closes[day_idxs]/tick).astype(np.int64).max()
        bins_  = int(hi_px_-lo_px_+1)
        run_vol = np.zeros(bins_, dtype=np.float64); run_tv = 0.0
        for k in range(nb):
            bar_i = day_idxs[k]
            px_k  = int(np.round(closes[bar_i]/tick)) - lo_px_
            if k >= MIN_PROFILE_BARS and run_tv > 0:
                poc_b = int(np.argmax(run_vol))
                tgt = run_tv*VA_PCT; cum = run_vol[poc_b]
                lo_i2=poc_b; hi_i2=poc_b
                while cum < tgt:
                    can_lo=lo_i2>0; can_hi=hi_i2<bins_-1
                    if not can_lo and not can_hi: break
                    al=run_vol[lo_i2-1] if can_lo else -1.
                    ah=run_vol[hi_i2+1] if can_hi else -1.
                    if al>=ah: lo_i2-=1; cum+=run_vol[lo_i2]
                    else:       hi_i2+=1; cum+=run_vol[hi_i2]
                val_arr[bar_i] = float((lo_i2+lo_px_)*tick)
                vah_arr[bar_i] = float((hi_i2+lo_px_)*tick)
            if 0<=px_k<bins_:
                run_vol[px_k] += volumes[day_idxs[k]]
            run_tv += volumes[day_idxs[k]]
    sorted_days = sorted(completed.keys())
    day_val_vah = {d: completed[d] for d in sorted_days}
    for i in range(n):
        if not np.isnan(val_arr[i]): continue
        today = pd.Timestamp(dates[i])
        idx_  = bisect.bisect_left(sorted_days, today)
        if idx_ > 0:
            pd_ = sorted_days[idx_-1]
            val_arr[i], vah_arr[i] = day_val_vah[pd_]
    return val_arr, vah_arr

def compute_all_zscores(closes, volumes, atr, window):
    kw   = dict(min_periods=window, closed="left")
    c_s  = pd.Series(closes); v_s = pd.Series(volumes)
    rm   = c_s.rolling(window,**kw).mean().values
    rs   = c_s.rolling(window,**kw).std(ddof=1).values
    z_rolling = np.where(rs>0, (closes-rm)/rs, np.nan)
    lc   = np.log(np.clip(closes,1e-9,None)); lc_s=pd.Series(lc)
    lm   = lc_s.rolling(window,**kw).mean().values
    ls   = lc_s.rolling(window,**kw).std(ddof=1).values
    z_log = np.where(ls>0, (lc-lm)/ls, np.nan)
    # OLS detrended (vectorised)
    n_arr = len(closes)
    z_det = np.full(n_arr, np.nan)
    if window >= 2:
        try:
            x   = np.arange(window, dtype=np.float64)
            Sx  = x.sum(); Sx2=(x*x).sum(); den=window*Sx2-Sx**2
            if den != 0:
                wins = np.lib.stride_tricks.as_strided(
                    closes, shape=(n_arr-window,window),
                    strides=(closes.strides[0],closes.strides[0]))
                nm  = np.any(np.isnan(wins), axis=1)
                Sy  = wins.sum(1); Sxy=(wins*x).sum(1)
                sl  = (window*Sxy-Sx*Sy)/den; ic=(Sy-sl*Sx)/window
                fit = sl*(window-1)+ic
                res = wins-(sl[:,None]*x+ic[:,None])
                sr  = res.std(1,ddof=1)
                ok  = (~nm)&(sr>0)
                z_det[window:][ok] = (closes[window:][ok]-fit[ok])/sr[ok]
        except Exception:
            pass
    z_atr = np.where(atr>0, (closes-rm)/atr, np.nan)
    cv2  = (c_s*v_s).rolling(window,**kw).sum().values
    sv   = v_s.rolling(window,**kw).sum().values
    vwap = np.where(sv>0, cv2/sv, np.nan)
    dev  = closes-vwap; dev_s=pd.Series(dev)
    vstd = dev_s.rolling(window,**kw).std(ddof=1).values
    z_vwap = np.where(vstd>0, dev/vstd, np.nan)
    return {"z_rolling":z_rolling,"z_log":z_log,"z_detrended":z_det,
            "z_atr":z_atr,"z_vwap":z_vwap}

def precompute_confirmations(arr_open, arr_high, arr_low, arr_close,
                              arr_vol, arr_val, arr_vah):
    n = len(arr_close)
    lo = np.zeros(n, dtype=np.int8); so = np.zeros(n, dtype=np.int8)
    body       = np.abs(arr_close-arr_open)
    lower_wick = np.maximum(arr_open,arr_close)-arr_low
    upper_wick = arr_high-np.minimum(arr_open,arr_close)
    lo += ((lower_wick>body*1.5)&(~np.isnan(arr_val))&(arr_close>arr_val)).astype(np.int8)
    so += ((upper_wick>body*1.5)&(~np.isnan(arr_vah))&(arr_close<arr_vah)).astype(np.int8)
    vol_s   = pd.Series(arr_vol)
    vol_avg = vol_s.rolling(5,min_periods=3,closed="left").mean().values
    quiet   = ((~np.isnan(vol_avg))&(vol_avg>0)&(arr_vol<vol_avg*0.70))
    lo += quiet.astype(np.int8); so += quiet.astype(np.int8)
    cur_range  = arr_high-arr_low
    prev_range = np.empty(n,dtype=np.float64)
    prev_range[0]=np.nan; prev_range[1:]=cur_range[:-1]
    narrow = ((~np.isnan(prev_range))&(prev_range>0)&(cur_range<prev_range*0.70))
    lo += narrow.astype(np.int8); so += narrow.astype(np.int8)
    lo += ((~np.isnan(arr_val))&(arr_close>arr_val)).astype(np.int8)
    so += ((~np.isnan(arr_vah))&(arr_close<arr_vah)).astype(np.int8)
    return lo, so


