def simulate_trades(arr_open, arr_high, arr_low, arr_close,
                    arr_z, arr_atr, arr_val, arr_vah,
                    confirm_scores, z_low, z_high,
                    direction, max_hold, bar_timestamps):
    n = len(arr_close); trades = []
    valid = (~np.isnan(arr_z)&~np.isnan(arr_atr)&(arr_atr>0)&
             ~np.isnan(arr_val)&~np.isnan(arr_vah))
    if direction == "LONG":
        z_ok     = (arr_z<=z_high) if np.isinf(z_low) else ((arr_z>=z_low)&(arr_z<=z_high))
        entry_px = arr_val; price_ok = arr_low<=arr_val
    else:
        z_ok     = (arr_z>=z_low) if np.isinf(z_high) else ((arr_z>=z_low)&(arr_z<=z_high))
        entry_px = arr_vah; price_ok = arr_high>=arr_vah
    confirm_ok = (confirm_scores>=MIN_CONFIRM_SCORE)
    sig_bars   = np.where(valid&z_ok&price_ok&confirm_ok)[0]
    if len(sig_bars) == 0: return []

    open_count=0; used_until=-1; locked={}
    bar_dates = np.array([ts.date() for ts in bar_timestamps])

    def _lock(p):   locked[round(p,2)]=True
    def _unlock(p): locked.pop(round(p,2),None)
    def _locked(p): return any(abs(round(p,2)-k)<=ZONE_TOLERANCE for k in locked)

    for i in sig_bars:
        if i<=used_until or open_count>=MAX_TRADES_PER_SIDE: continue
        ep=entry_px[i]; atr=arr_atr[i]
        if np.isnan(ep) or ep<=0: continue
        ref = arr_val[i] if direction=="LONG" else arr_vah[i]
        if _locked(ref): continue
        sl_pts=SL_ATR_CAP*atr; tp_pts=RR*sl_pts
        sl_p=(ep-sl_pts) if direction=="LONG" else (ep+sl_pts)
        tp_p=(ep+tp_pts) if direction=="LONG" else (ep-tp_pts)
        cost_rt=COST_PCT_RT*ep
        _lock(ref); open_count+=1
        outcome="timeout"; exit_bar=min(i+max_hold,n-1); exit_price=arr_close[exit_bar]
        for j in range(i, min(i+max_hold+1,n)):
            bopen=arr_open[j]
            if j>i and bar_dates[j]!=bar_dates[j-1]:
                if direction=="LONG":
                    if bopen<=sl_p: outcome="gap_stop";  exit_price=bopen; exit_bar=j; break
                    if bopen>=tp_p: outcome="gap_target"; exit_price=bopen; exit_bar=j; break
                else:
                    if bopen>=sl_p: outcome="gap_stop";  exit_price=bopen; exit_bar=j; break
                    if bopen<=tp_p: outcome="gap_target"; exit_price=bopen; exit_bar=j; break
            if direction=="LONG":
                if arr_low[j]<=sl_p:  outcome="stop";  exit_price=sl_p; exit_bar=j; break
                if arr_high[j]>=tp_p: outcome="target"; exit_price=tp_p; exit_bar=j; break
            else:
                if arr_high[j]>=sl_p: outcome="stop";  exit_price=sl_p; exit_bar=j; break
                if arr_low[j]<=tp_p:  outcome="target"; exit_price=tp_p; exit_bar=j; break
        pnl_gross=((exit_price-ep) if direction=="LONG" else (ep-exit_price))
        pnl_net=pnl_gross-cost_rt
        _unlock(ref); open_count-=1; used_until=exit_bar
        trades.append({
            "direction":direction,"entry_bar":i,"exit_bar":exit_bar,
            "entry_ts":bar_timestamps[i],"exit_ts":bar_timestamps[exit_bar],
            "entry_price":ep,"exit_price":exit_price,"atr":atr,
            "val":arr_val[i],"vah":arr_vah[i],"sl_pts":sl_pts,"tp_pts":tp_pts,
            "outcome":outcome,"pnl_gross":pnl_gross,"cost_rt":cost_rt,
            "pnl_pts":pnl_net,"win":pnl_net>0,"hold_bars":exit_bar-i,
            "z_entry":arr_z[i],"is_overnight":bool(bar_dates[exit_bar]!=bar_dates[i]),
        })
    return trades

def calc_metrics(trades, backtest_years=1.0):
    if len(trades)<MIN_TRADES: return None
    df=pd.DataFrame(trades); n=len(df)
    w=df[df["win"]]; l=df[~df["win"]]
    wr=len(w)/n
    aw=float(w["pnl_pts"].mean()) if len(w)>0 else 0.0
    al=float(l["pnl_pts"].mean()) if len(l)>0 else 0.0
    gp=float(w["pnl_pts"].sum()); gl=float(abs(l["pnl_pts"].sum()))
    pf=gp/gl if gl>0 else np.inf
    if wr<MIN_WIN_RATE or pf<MIN_PROFIT_FACTOR: return None
    exp=wr*aw+(1-wr)*al
    avg_annual=n/max(backtest_years,0.1)
    return {"n_trades":n,"win_rate":wr,"avg_annual_trades":round(avg_annual,1),
            "avg_win_pts":aw,"avg_loss_pts":al,"expectancy_pts":exp,
            "profit_factor":pf,"raw_trades":trades}


