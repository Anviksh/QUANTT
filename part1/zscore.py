def fetch_alpaca_hourly_gnn(tickers, start, end=None):
    """Fetch hourly bars for GNN — returns {ticker: DataFrame}."""
    try:
        import alpaca_trade_api as tradeapi
    except ImportError:
        raise ImportError("pip install alpaca-trade-api")
    api = tradeapi.REST(ALPACA_API_KEY, ALPACA_API_SECRET,
                        base_url="https://paper-api.alpaca.markets")
    if end is None:
        end = datetime.now(UTC).strftime("%Y-%m-%d")
    CHUNK = 365
    all_data = {}
    for ticker in tickers:
        frames = []
        cur_s = pd.Timestamp(start, tz="UTC")
        cur_e = pd.Timestamp(end,   tz="UTC")
        while cur_s < cur_e:
            chunk_e = min(cur_s + pd.Timedelta(days=CHUNK), cur_e)
            try:
                bars = api.get_bars(ticker, "1Hour",
                    start=cur_s.isoformat(), end=chunk_e.isoformat(),
                    feed="iex", adjustment="all").df
                if not bars.empty:
                    bars.index = pd.to_datetime(bars.index, utc=True)
                    frames.append(bars[["open","high","low","close","volume"]].copy())
            except Exception as e:
                print(f"  Warning {ticker}: {e}")
            cur_s = chunk_e + pd.Timedelta(hours=1)
        if frames:
            df = pd.concat(frames).sort_index()
            df = df[~df.index.duplicated(keep="last")]
            all_data[ticker] = df
            print(f"  {ticker}: {len(df)} hourly bars")
        else:
            print(f"  {ticker}: NO DATA")
    return all_data


