def train_regime_xgb_models(train_df, fold_id, scalers_dict=None):
    """
    Train one XGBRanker per regime on the training fold.
    Returns dict {regime: model}, and scalers dict.
    """
    print(f"\n[XGBoost Training] Fold {fold_id}")
    models  = {}
    scalers = scalers_dict or {}

    for regime in range(N_REGIMES):
        regime_df = train_df[train_df["gnn_regime_raw"] == regime].copy()
        n_regime  = len(regime_df)
        print(f"\n  Regime {regime} ({REGIME_NAMES[regime]}): {n_regime} trades", end="")

        if n_regime < MIN_REGIME_TRADES_FOR_MODEL:
            print(f"  ⚠  < {MIN_REGIME_TRADES_FOR_MODEL} trades — using global fallback")
            models[regime] = None
            continue

        # SMOTE for stressed regimes (regime 4, optionally 3)
        X_raw = regime_df[FEATURE_COLS].copy()
        X_raw = X_raw.replace([np.inf,-np.inf], np.nan).fillna(X_raw.median()).clip(-10,10)
        y_raw = regime_df["target"].values.astype(np.float32)

        if regime == 4 and SMOTE_OK and n_regime < 300:
            # Convert to binary classification for SMOTE then revert
            try:
                threshold = np.median(y_raw)
                y_bin     = (y_raw > threshold).astype(int)
                sm        = SMOTE(k_neighbors=min(5, n_regime-1), random_state=42)
                X_sm, y_bin_sm = sm.fit_resample(X_raw.values, y_bin)
                # Rebuild continuous target: assign mean target of original class
                mean_pos = y_raw[y_bin == 1].mean() if (y_bin==1).any() else 0.1
                mean_neg = y_raw[y_bin == 0].mean() if (y_bin==0).any() else -0.1
                y_sm     = np.where(y_bin_sm == 1, mean_pos, mean_neg).astype(np.float32)
                # Inject jitter to avoid degenerate groups
                y_sm     += np.random.normal(0, abs(y_raw.std()*0.05), len(y_sm))
                X_raw_v   = X_sm
                y_raw     = y_sm
                print(f"  → SMOTE {n_regime} → {len(y_raw)}", end="")
            except Exception as e:
                print(f"  ⚠  SMOTE failed ({e}), using original", end="")
                X_raw_v = X_raw.values
        else:
            X_raw_v = X_raw.values

        # Fit scaler on training regime data
        if regime not in scalers:
            sc = RobustScaler()
            sc.fit(X_raw_v)
            scalers[regime] = sc
        else:
            sc = scalers[regime]

        X_scaled = np.clip(sc.transform(X_raw_v), -5, 5)

        # Validation split (last 10% chronologically)
        n_val = max(int(len(X_scaled) * XGB_VAL_FRACTION), 10)
        X_tr, X_val = X_scaled[:-n_val], X_scaled[-n_val:]
        y_tr, y_val = y_raw[:-n_val],    y_raw[-n_val:]

        # Groups for ranker: simple approach — each group = 1 query = 1 row
        # (because regime slices lose the date alignment; use groups of 1)
        grp_tr  = [1] * len(X_tr)
        grp_val = [1] * len(X_val)

        # Adjust params for regime
        params = dict(XGB_PARAMS_BASE)
        if regime in [3, 4]:
            params["min_child_weight"] = max(1, n_regime // 30)
            params["max_depth"]        = 4   # less complex for sparse regimes
        if regime == 4:
            params["n_estimators"] = 200

        model = xgb.XGBRanker(**params)
        try:
            model.fit(
                X_tr, y_tr,
                group=grp_tr,
                eval_set=[(X_val, y_val)],
                eval_group=[grp_val],
                verbose=False,
                early_stopping_rounds=XGB_EARLY_STOPPING,
            )
            print(f"  ✓ best_ntree={model.best_iteration}  "
                  f"best_score={model.best_score:.5f}", end="")
        except Exception as e:
            # XGBRanker sometimes fails if group structure is degenerate
            # Fall back to XGBRegressor
            print(f"  ⚠  Ranker failed ({e}) → falling back to XGBRegressor", end="")
            params_reg = {k: v for k, v in params.items()
                          if k not in ["objective","eval_metric"]}
            params_reg["objective"]   = "reg:squarederror"
            params_reg["eval_metric"] = "rmse"
            model = xgb.XGBRegressor(**params_reg)
            model.fit(
                X_tr, y_tr,
                eval_set=[(X_val, y_val)],
                verbose=False,
                early_stopping_rounds=XGB_EARLY_STOPPING,
            )
            print(f"  ✓ regressor best_ntree={model.best_iteration}", end="")

        models[regime] = model
        # Save model
        model_path = os.path.join(XGB_OUT_DIR, "models",
                                  f"xgb_regime_{regime}_fold_{fold_id}.json")
        model.save_model(model_path)
        print(f"  💾 saved")

    # Train global fallback model (all regimes combined)
    print(f"\n  Training global fallback model ...")
    X_all = train_df[FEATURE_COLS].copy()
    X_all = X_all.replace([np.inf,-np.inf], np.nan).fillna(X_all.median()).clip(-10,10)
    y_all = train_df["target"].values.astype(np.float32)
    if "global" not in scalers:
        sc_g = RobustScaler(); sc_g.fit(X_all.values)
        scalers["global"] = sc_g
    X_all_sc = np.clip(scalers["global"].transform(X_all.values), -5, 5)
    n_val_g  = max(int(len(X_all_sc) * XGB_VAL_FRACTION), 10)
    global_model = xgb.XGBRegressor(
        **{k: v for k, v in XGB_PARAMS_BASE.items()
           if k not in ["objective","eval_metric"]},
        objective="reg:squarederror", eval_metric="rmse"
    )
    global_model.fit(
        X_all_sc[:-n_val_g], y_all[:-n_val_g],
        eval_set=[(X_all_sc[-n_val_g:], y_all[-n_val_g:])],
        verbose=False,
        early_stopping_rounds=XGB_EARLY_STOPPING,
    )
    models["global"] = global_model
    global_model.save_model(
        os.path.join(XGB_OUT_DIR, "models", f"xgb_global_fold_{fold_id}.json"))
    print(f"  ✓ global model  [{elapsed()}]")

    return models, scalers


