def train_gnn(graphs_scaled, dates_v, train_mask, val_mask, n_nodes, n_node_feat):
    print("\n[GNN Training]")
    model     = MarketGNN(n_node_feat, n_nodes).to(DEVICE)
    optimizer = torch.optim.Adam(model.parameters(), lr=GNN_LR, weight_decay=GNN_WD)

    # FIX: 'verbose' kwarg was removed in PyTorch >= 2.4
    # Use a try/except to support both old and new versions
    try:
        scheduler = ReduceLROnPlateau(optimizer, mode="min", patience=10,
                                      factor=0.5, verbose=False)
    except TypeError:
        # PyTorch >= 2.4 removed verbose from ReduceLROnPlateau
        scheduler = ReduceLROnPlateau(optimizer, mode="min", patience=10, factor=0.5)

    criterion = nn.MSELoss()
    best_val   = float("inf")
    best_st    = None
    patience_c = 0
    train_losses, val_losses = [], []

    oos_ts     = pd.Timestamp("2024-01-01", tz="UTC")
    train_only = [i for i in train_mask if dates_v[i] < oos_ts]
    val_only   = [i for i in train_mask if dates_v[i] >= oos_ts]

    print(f"  n_nodes={n_nodes}  n_node_feat={n_node_feat}")
    print(f"  Train graphs : {len(train_only)}  Val graphs : {len(val_only)}")
    print(f"  Parameters   : {sum(p.numel() for p in model.parameters()):,}")

    def run_epoch(idx_list, train=True):
        if train: np.random.shuffle(idx_list); model.train()
        else: model.eval()
        total = 0.0
        for bs in range(0, len(idx_list), GNN_BATCH_SIZE):
            batch_idx    = idx_list[bs: bs+GNN_BATCH_SIZE]
            batch_graphs = [graphs_scaled[i] for i in batch_idx]
            if PYGEOM_OK:
                bd   = graphs_to_batch_pygeom(batch_graphs).to(DEVICE)
                Xo   = torch.stack([torch.tensor(g[0],dtype=torch.float32).to(DEVICE)
                                    for g in batch_graphs])
                if train: optimizer.zero_grad()
                with torch.set_grad_enabled(train):
                    z, recon, _ = model.forward_pygeom(bd)
                    loss = criterion(recon, Xo)
            else:
                xl   = [torch.tensor(g[0],dtype=torch.float32).to(DEVICE) for g in batch_graphs]
                al   = [make_dense_adj(n_nodes,g[1],g[2],DEVICE) for g in batch_graphs]
                Xo   = torch.stack(xl)
                if train: optimizer.zero_grad()
                with torch.set_grad_enabled(train):
                    z, recon = model.forward_manual(xl, al)
                    loss = criterion(recon, Xo)
            if train:
                loss.backward()
                nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
            total += loss.item() * len(batch_idx)
        return total / max(len(idx_list), 1)

    for epoch in range(1, GNN_MAX_EPOCHS+1):
        t_loss = run_epoch(list(train_only), train=True)
        v_loss = run_epoch(list(val_only),   train=False)
        scheduler.step(v_loss)
        train_losses.append(t_loss); val_losses.append(v_loss)
        if v_loss < best_val:
            best_val = v_loss
            best_st  = {k: v.cpu().clone() for k,v in model.state_dict().items()}
            patience_c = 0
        else:
            patience_c += 1
        if epoch % 20 == 0 or epoch == 1:
            lr_now = optimizer.param_groups[0]["lr"]
            print(f"  Epoch {epoch:03d}: train={t_loss:.6f}  val={v_loss:.6f}  "
                  f"best={best_val:.6f}  patience={patience_c}/{GNN_PATIENCE}  lr={lr_now:.6f}")
        if patience_c >= GNN_PATIENCE:
            print(f"  Early stopping at epoch {epoch}")
            break

    model.load_state_dict(best_st)
    print(f"\n  ✓ Training done. Best val loss: {best_val:.6f}")
    return model, train_losses, val_losses


