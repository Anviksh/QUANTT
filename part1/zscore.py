def compute_portfolio_metrics(trade_log, capital=STARTING_CAPITAL, backtest_years=1.0):
    if trade_log.empty: return {}
    tl = trade_log.sort_values("exit_ts").reset_index(drop=True)
    n  = len(tl)
    equity = np.zeros(n+1); equity[0] = capital
    for i, pnl in enumerate(tl["pnl_usd"].values):
        equity[i+1] = equity[i] + pnl
    final_equity = equity[-1]; net_pnl = final_equity-capital
    ret_pct = net_pnl/capital*100.0
    cagr    = ((final_equity/capital)**(1.0/max(backtest_years,0.1))-1.0)*100.0
    peak    = np.maximum.accumulate(equity)
    dd_pct  = (equity-peak)/peak*100.0
    max_dd  = float(np.min(dd_pct))
    calmar  = cagr/abs(max_dd) if max_dd!=0 else np.nan
    prior_eq = equity[:-1]
    trade_ret= tl["pnl_usd"].values/np.where(prior_eq>0,prior_eq,1.0)
    tpy = n/max(backtest_years,0.1); ann_f = np.sqrt(max(tpy,1))
    mean_r=trade_ret.mean(); std_r=trade_ret.std(ddof=1) if len(trade_ret)>1 else 1e-9
    sharpe = (mean_r/std_r)*ann_f if std_r>0 else 0.0
    wins=tl[tl["win"]]["pnl_usd"]; losses=tl[~tl["win"]]["pnl_usd"]
    win_rate=tl["win"].mean()
    avg_win=wins.mean() if len(wins)>0 else 0.0
    avg_loss=losses.mean() if len(losses)>0 else 0.0
    gp=wins.sum(); gl=abs(losses.sum())
    pf=gp/gl if gl>0 else np.inf
    exp=win_rate*avg_win+(1-win_rate)*avg_loss
    total_costs=tl["cost_usd"].sum() if "cost_usd" in tl.columns else 0.0
    return {"starting_capital":capital,"final_equity":final_equity,
            "net_pnl":net_pnl,"return_pct":ret_pct,"cagr_pct":cagr,
            "max_drawdown_pct":max_dd,"calmar_ratio":calmar,"sharpe_ratio":sharpe,
            "n_trades":n,"win_rate":win_rate,"avg_win_usd":avg_win,
            "avg_loss_usd":avg_loss,"expectancy_usd":exp,
            "profit_factor":pf,"total_costs_usd":total_costs,
            "_equity":equity,"_dd_pct":dd_pct,"_trade_log":tl}

