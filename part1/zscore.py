def build_hawkes_analysis(fold_results):
    """Summarise Hawkes gate effectiveness across folds."""
    rows = []
    for fold_id, res in fold_results.items():
        rows.append({
            "fold":               fold_id,
            "hawkes_filter_pct":  res.get("hawkes_filtered_pct", 0),
            "sharpe":             res.get("sharpe", 0),
            "calmar":             res.get("calmar", 0),
            "win_rate":           res.get("win_rate", 0),
            "net_pnl":            res.get("net_pnl", 0),
            "n_trades":           res.get("n_trades", 0),
        })
    df = pd.DataFrame(rows)
    df.to_csv(os.path.join("results","final_summary",
                            "hawkes_filter_analysis.csv"), index=False)
    return df


def build_regime_breakdown(combined_oos):
    """Per-regime P&L, win rate, trade count across all OOS folds."""
    if combined_oos.empty or "gnn_regime_raw" not in combined_oos.columns:
        return pd.DataFrame()
    rows = []
    for r in range(N_REGIMES):
        sub = combined_oos[combined_oos["gnn_regime_raw"] == r]
        if sub.empty: continue
        pnl    = sub["pnl_usd"].sum() if "pnl_usd" in sub else 0
        wr     = sub["win"].mean()    if "win"    in sub else 0
        n      = len(sub)
        rows.append({
            "regime":       r,
            "regime_name":  REGIME_NAMES[r],
            "n_trades":     n,
            "win_rate":     round(wr * 100, 2),
            "total_pnl_usd": round(pnl, 2),
            "avg_pnl_usd":  round(pnl/max(n,1), 2),
        })
    df = pd.DataFrame(rows)
    df.to_csv(os.path.join("results","final_summary",
                            "regime_performance_breakdown.csv"), index=False)
    return df


hawkes_analysis_df  = build_hawkes_analysis(fold_results)
