# ── Cell 7 : ATR ──────────────────────────────────────────────

def attach_atr(df_1h: pd.DataFrame, period: int = ATR_LOOKBACK) -> pd.DataFrame:
    daily = df_1h.resample("1D").agg(
        high=("high","max"), low=("low","min"), close=("close","last")).dropna()
    pc  = daily["close"].shift(1)
    tr  = pd.concat([daily["high"]-daily["low"],
                     (daily["high"]-pc).abs(),
                     (daily["low"]-pc).abs()], axis=1).max(axis=1)
    atr = tr.ewm(alpha=1.0/period, adjust=False).mean().shift(1).ffill()
    atr.index = atr.index.normalize()
    out = df_1h.copy()
    out["__d__"] = out.index.normalize()
    out["atr"]   = out["__d__"].map(atr)
    out.drop(columns=["__d__"], inplace=True)
    return out


# ── Cell 8 : Volume Profile ───────────────────────────────────

@dataclass
class SessionProfile:
    val: float; vah: float; poc: float; n_bars: int

def _compute_profile_vectorised(closes, volumes):
    if len(closes) == 0: return None
    tv = volumes.sum()
    if tv <= 0: return None
    tick = 0.01
    px   = np.round(closes / tick).astype(np.int64)
    lo, hi = px.min(), px.max(); bins = hi - lo + 1
    vol_at_px = np.zeros(bins, dtype=np.float64)
    np.add.at(vol_at_px, px - lo, volumes)
    poc_idx = int(np.argmax(vol_at_px))
    poc = float((poc_idx + lo) * tick)
    target = tv * VA_PCT; cum = vol_at_px[poc_idx]
    lo_i = poc_idx; hi_i = poc_idx
    while cum < target:
        can_lo = lo_i > 0; can_hi = hi_i < bins - 1
        if not can_lo and not can_hi: break
        add_lo = vol_at_px[lo_i-1] if can_lo else -1.0
        add_hi = vol_at_px[hi_i+1] if can_hi else -1.0
        if add_lo >= add_hi: lo_i -= 1; cum += vol_at_px[lo_i]
        else:                hi_i += 1; cum += vol_at_px[hi_i]
    return SessionProfile(float((lo_i+lo)*tick), float((hi_i+lo)*tick), poc, len(closes))

def precompute_profiles(df):
    et_idx  = df.index.tz_convert(EASTERN)
    dates   = et_idx.normalize()
    closes  = df["close"].values.astype(np.float64)
    volumes = df["volume"].values.astype(np.float64)
    n = len(df)
    val_arr = np.full(n, np.nan, dtype=np.float64)
    vah_arr = np.full(n, np.nan, dtype=np.float64)
    completed = {}; tick = 0.01
    for day in dates.unique():
        mask = (dates == day); day_idxs = np.where(mask)[0]
        if len(day_idxs) == 0: continue
        day_closes  = closes[day_idxs]
        day_volumes = volumes[day_idxs]
        nb = len(day_idxs)
        p_full = _compute_profile_vectorised(day_closes, day_volumes)
        if p_full is not None: completed[pd.Timestamp(day)] = p_full
        px_all = np.round(day_closes / tick).astype(np.int64)
        if nb == 0: continue
        lo_px = px_all.min(); hi_px = px_all.max(); bins = int(hi_px - lo_px + 1)
        running_vol = np.zeros(bins, dtype=np.float64); running_tv = 0.0
        for k in range(nb):
            bar_i = day_idxs[k]
            if k >= MIN_PROFILE_BARS and running_tv > 0:
                poc_b = int(np.argmax(running_vol))
                target = running_tv * VA_PCT
                cum = running_vol[poc_b]; lo_i = poc_b; hi_i = poc_b
                while cum < target:
                    can_lo = lo_i > 0; can_hi = hi_i < bins - 1
                    if not can_lo and not can_hi: break
                    add_lo = running_vol[lo_i-1] if can_lo else -1.0
                    add_hi = running_vol[hi_i+1] if can_hi else -1.0
                    if add_lo >= add_hi: lo_i -= 1; cum += running_vol[lo_i]
                    else:               hi_i += 1; cum += running_vol[hi_i]
                val_arr[bar_i] = float((lo_i + lo_px) * tick)
                vah_arr[bar_i] = float((hi_i + lo_px) * tick)
            b_idx = int(px_all[k] - lo_px)
            running_vol[b_idx] += day_volumes[k]; running_tv += day_volumes[k]
    sorted_days = sorted(completed.keys())
    day_val_vah = {d: (completed[d].val, completed[d].vah) for d in sorted_days}
    for i in range(n):
        if not np.isnan(val_arr[i]): continue
        today = pd.Timestamp(dates[i])
        idx   = bisect.bisect_left(sorted_days, today)
        if idx > 0:
            prev_day = sorted_days[idx-1]
            val_arr[i], vah_arr[i] = day_val_vah[prev_day]
    return val_arr, vah_arr, completed


# ── Cell 9 : Z-Score Formulas ─────────────────────────────────

def _ols_zscore_vectorised(cv, w):
    n = len(cv); out = np.full(n, np.nan, dtype=np.float64)
    if w < 2: return out
    x = np.arange(w, dtype=np.float64)
    Sx = x.sum(); Sx2 = (x*x).sum(); den = w*Sx2 - Sx*Sx
    if den == 0: return out
    try:
        wins = np.lib.stride_tricks.as_strided(
            cv, shape=(n-w, w), strides=(cv.strides[0], cv.strides[0]))
    except Exception:
        wins = np.array([cv[i:i+w] for i in range(n-w)])
    nm  = np.any(np.isnan(wins), axis=1)
    Sy  = wins.sum(axis=1); Sxy = (wins * x).sum(axis=1)
    sl  = (w*Sxy - Sx*Sy) / den; ic = (Sy - sl*Sx) / w; fit = sl*w + ic
    res = wins - (sl[:,None]*x[None,:] + ic[:,None])
    sr  = res.std(axis=1, ddof=1)
    ci  = cv[w:]; ok = (~nm) & (sr > 0)
    out[w:][ok] = (ci[ok] - fit[ok]) / sr[ok]
    return out

def compute_all_zscores(closes, volumes, atr, window):
    kw = dict(min_periods=window, closed="left")
    c_s = pd.Series(closes); v_s = pd.Series(volumes)
    rm  = c_s.rolling(window, **kw).mean().values
    rs  = c_s.rolling(window, **kw).std(ddof=1).values
    z_rolling = np.where(rs > 0, (closes-rm)/rs, np.nan)
    lc  = np.log(np.clip(closes, 1e-9, None)); lc_s = pd.Series(lc)
    lm  = lc_s.rolling(window, **kw).mean().values
    ls  = lc_s.rolling(window, **kw).std(ddof=1).values
    z_log = np.where(ls > 0, (lc-lm)/ls, np.nan)
    z_detrended = _ols_zscore_vectorised(closes.astype(np.float64), window)
    z_atr = np.where(atr > 0, (closes-rm)/atr, np.nan)
    cv2 = (c_s * v_s).rolling(window, **kw).sum().values
    sv  = v_s.rolling(window, **kw).sum().values
    vwap = np.where(sv > 0, cv2/sv, np.nan)
    dev  = closes - vwap; dev_s = pd.Series(dev)
    vstd = dev_s.rolling(window, **kw).std(ddof=1).values
    z_vwap = np.where(vstd > 0, dev/vstd, np.nan)
    return {"z_rolling": z_rolling, "z_log": z_log, "z_detrended": z_detrended,
            "z_atr": z_atr, "z_vwap": z_vwap}


# ── Cell 10 : Confirmation Signals ────────────────────────────

def precompute_confirmations(arr_open, arr_high, arr_low, arr_close,
                              arr_vol, arr_val, arr_vah):
    n = len(arr_close)
    long_score  = np.zeros(n, dtype=np.int8)
    short_score = np.zeros(n, dtype=np.int8)
    body        = np.abs(arr_close - arr_open)
    lower_wick  = np.maximum(arr_open, arr_close) - arr_low
    upper_wick  = arr_high - np.minimum(arr_open, arr_close)
    c1_long  = ((lower_wick > body*1.5) & (~np.isnan(arr_val)) & (arr_close > arr_val))
    c1_short = ((upper_wick > body*1.5) & (~np.isnan(arr_vah)) & (arr_close < arr_vah))
    long_score  += c1_long.astype(np.int8)
    short_score += c1_short.astype(np.int8)
    vol_s   = pd.Series(arr_vol)
    vol_avg = vol_s.rolling(5, min_periods=3, closed="left").mean().values
    c2 = ((~np.isnan(vol_avg)) & (vol_avg > 0) & (arr_vol < vol_avg * 0.70))
    long_score  += c2.astype(np.int8)
    short_score += c2.astype(np.int8)
    cur_range  = arr_high - arr_low
    prev_range = np.empty(n, dtype=np.float64)
    prev_range[0] = np.nan; prev_range[1:] = cur_range[:-1]
    c3 = ((~np.isnan(prev_range)) & (prev_range > 0) & (cur_range < prev_range * 0.70))
    long_score  += c3.astype(np.int8)
    short_score += c3.astype(np.int8)
    c4_long  = ((~np.isnan(arr_val)) & (arr_close > arr_val))
    c4_short = ((~np.isnan(arr_vah)) & (arr_close < arr_vah))
    long_score  += c4_long.astype(np.int8)
    short_score += c4_short.astype(np.int8)
    return long_score, short_score


# ── Cell 11 : Trade Simulator — with OVERNIGHT GAP FIX ────────

def simulate_trades(arr_open, arr_high, arr_low, arr_close,
                    arr_z, arr_atr, arr_val, arr_vah,
                    confirm_scores, z_low, z_high,
                    direction, max_hold, bar_timestamps) -> List[dict]:
    n = len(arr_close); trades = []
    valid = (~np.isnan(arr_z) & ~np.isnan(arr_atr) & (arr_atr > 0) &
             ~np.isnan(arr_val) & ~np.isnan(arr_vah))
    if direction == "LONG":
        z_ok       = (arr_z <= z_high) if np.isinf(z_low) else ((arr_z >= z_low) & (arr_z <= z_high))
        entry_px_all = arr_val
        price_ok   = arr_low <= arr_val
    else:
        z_ok       = (arr_z >= z_low) if np.isinf(z_high) else ((arr_z >= z_low) & (arr_z <= z_high))
        entry_px_all = arr_vah
        price_ok   = arr_high >= arr_vah
    confirm_ok = (confirm_scores >= MIN_CONFIRM_SCORE)
    sig_bars   = np.where(valid & z_ok & price_ok & confirm_ok)[0]
    if len(sig_bars) == 0: return []

    open_count  = 0
    locked_zones: dict = {}
    used_until  = -1

    def _zk(p):        return round(p, 2)
    def _locked(ref):  return any(abs(_zk(ref)-lk) <= ZONE_TOLERANCE for lk in locked_zones)
    def _lock(ref):    locked_zones[_zk(ref)] = True
    def _unlock(ref):  locked_zones.pop(_zk(ref), None)

    bar_dates = np.array([ts.date() for ts in bar_timestamps])

    for i in sig_bars:
        if i <= used_until or open_count >= MAX_TRADES_PER_SIDE: continue
        ep  = entry_px_all[i]; atr = arr_atr[i]
        val = arr_val[i];      vah = arr_vah[i]
        ref = val if direction == "LONG" else vah
        if np.isnan(ep) or ep <= 0: continue
        if _locked(ref): continue

        sl_pts = SL_ATR_CAP * atr; tp_pts = RR * sl_pts
        sl_p   = (ep - sl_pts) if direction == "LONG" else (ep + sl_pts)
        tp_p   = (ep + tp_pts) if direction == "LONG" else (ep - tp_pts)
        cost_rt = COST_PCT_RT * ep

        _lock(ref); open_count += 1
        mae = 0.0; mfe = 0.0
        outcome    = "timeout"
        exit_bar   = min(i + max_hold, n - 1)
        exit_price = arr_close[exit_bar]

        for j in range(i, min(i + max_hold + 1, n)):
            bopen = arr_open[j]
            bh    = arr_high[j]
            bl    = arr_low[j]

            if j > i and bar_dates[j] != bar_dates[j-1]:
                if direction == "LONG":
                    if bopen <= sl_p:
                        outcome = "gap_stop"; exit_price = bopen; exit_bar = j; break
                    elif bopen >= tp_p:
                        outcome = "gap_target"; exit_price = bopen; exit_bar = j; break
                else:
                    if bopen >= sl_p:
                        outcome = "gap_stop"; exit_price = bopen; exit_bar = j; break
                    elif bopen <= tp_p:
                        outcome = "gap_target"; exit_price = bopen; exit_bar = j; break

            if direction == "LONG":
                mae = max(mae, max(ep - bl, 0.0))
                mfe = max(mfe, max(bh - ep, 0.0))
                if bl <= sl_p: outcome = "stop";   exit_price = sl_p; exit_bar = j; break
                if bh >= tp_p: outcome = "target"; exit_price = tp_p; exit_bar = j; break
            else:
                mae = max(mae, max(bh - ep, 0.0))
                mfe = max(mfe, max(ep - bl, 0.0))
                if bh >= sl_p: outcome = "stop";   exit_price = sl_p; exit_bar = j; break
                if bl <= tp_p: outcome = "target"; exit_price = tp_p; exit_bar = j; break

        pnl_gross = (exit_price - ep) if direction == "LONG" else (ep - exit_price)
        pnl_net   = pnl_gross - cost_rt

        _unlock(ref); open_count -= 1; used_until = exit_bar

        trades.append({
            "direction":      direction,
            "entry_bar":      i,      "exit_bar":    exit_bar,
            "entry_ts":       bar_timestamps[i],
            "exit_ts":        bar_timestamps[exit_bar],
            "entry_price":    ep,     "exit_price":  exit_price,
            "atr":            atr,    "val":         val,    "vah":    vah,
            "sl_pts":         sl_pts, "tp_pts":      tp_pts,
            "sl_atr_mult":    sl_pts / atr,
            "mae_pts":        mae,    "mfe_pts":     mfe,
            "mae_atr":        mae / atr,
            "mfe_atr":        mfe / atr,
            "outcome":        outcome,
            "pnl_gross":      pnl_gross,
            "cost_rt":        cost_rt,
            "pnl_pts":        pnl_net,
            "win":            pnl_net > 0,
            "hold_bars":      exit_bar - i,
            "z_entry":        arr_z[i],
            "mfe_captured":   pnl_net / mfe if mfe > 0 else 0.0,
            "confirm_score":  int(confirm_scores[i]),
            "is_overnight":   bool(bar_dates[exit_bar] != bar_dates[i]),
        })
    return trades


# ── Cell 12 : Per-config Metrics ──────────────────────────────

def calc_metrics(trades, backtest_years=1.0):
    if len(trades) < MIN_TRADES: return None
    df = pd.DataFrame(trades); n = len(df)
    w  = df[df["win"]]; l = df[~df["win"]]
    wr = len(w) / n

    aw = float(w["pnl_pts"].mean()) if len(w) > 0 else 0.0
    al = float(l["pnl_pts"].mean()) if len(l) > 0 else 0.0
    gp = float(w["pnl_pts"].sum()); gl = float(abs(l["pnl_pts"].sum()))
    pf = gp / gl if gl > 0 else np.inf

    if wr < MIN_WIN_RATE:        return None
    if pf < MIN_PROFIT_FACTOR:   return None

    exp = wr * aw + (1 - wr) * al
    pr  = aw / abs(al) if al < 0 else 0.0
    ma  = df["mae_atr"].values; mf = df["mfe_atr"].values
    avg_annual = n / max(backtest_years, 0.1)

    return {
        "n_trades":          n,
        "win_rate":          wr,
        "avg_annual_trades": round(avg_annual, 1),
        "avg_win_pts":       aw,
        "avg_loss_pts":      al,
        "expectancy_pts":    exp,
        "profit_factor":     pf,
        "payoff_ratio":      pr,
        "breakeven_wr":      1.0 / (1.0 + RR),
        "mae_p50":           np.percentile(ma, 50),
        "mae_p95":           np.percentile(ma, 95),
        "mfe_p50":           np.percentile(mf, 50),
        "mfe_p95":           np.percentile(mf, 95),
        "mfe_captured_med":  float(np.median(df["mfe_captured"].values)),
        "hold_bars_med":     float(df["hold_bars"].median()),
        "stop_rate":         float((df["outcome"] == "stop").mean()),
        "target_rate":       float((df["outcome"] == "target").mean()),
        "timeout_rate":      float((df["outcome"] == "timeout").mean()),
        "gap_stop_rate":     float((df["outcome"] == "gap_stop").mean()),
        "gap_target_rate":   float((df["outcome"] == "gap_target").mean()),
        "overnight_pct":     float(df["is_overnight"].mean()),
        "raw_trades":        trades,
    }


# ── Cell 13 : Portfolio Metrics ───────────────────────────────

def compute_portfolio_metrics(trade_log, capital=STARTING_CAPITAL, backtest_years=1.0):
    if trade_log.empty: return {}
    tl = trade_log.sort_values("exit_ts").reset_index(drop=True)
    n  = len(tl)
    equity = np.zeros(n+1); equity[0] = capital
    for i, pnl in enumerate(tl["pnl_usd"].values):
        equity[i+1] = equity[i] + pnl
    final_equity = equity[-1]; net_pnl = final_equity - capital
    ret_pct = net_pnl / capital * 100.0
    cagr    = ((final_equity / capital) ** (1.0 / max(backtest_years, 0.1)) - 1.0) * 100.0
    peak    = np.maximum.accumulate(equity)
    dd      = equity - peak; dd_pct = dd / peak * 100.0
    max_dd  = float(np.min(dd)); max_dd_pct = float(np.min(dd_pct))
    calmar  = (cagr / abs(max_dd_pct)) if max_dd_pct != 0 else np.nan
    prior_eq = equity[:-1]
    trade_ret = tl["pnl_usd"].values / prior_eq
    trades_per_year = n / max(backtest_years, 0.1)
    ann_factor = np.sqrt(trades_per_year)
    mean_r = trade_ret.mean()
    std_r  = trade_ret.std(ddof=1) if len(trade_ret) > 1 else 1e-9
    sharpe = (mean_r / std_r) * ann_factor if std_r > 0 else 0.0
    down_r = trade_ret[trade_ret < 0]
    down_std = down_r.std(ddof=1) if len(down_r) > 1 else 1e-9
    sortino  = (mean_r / down_std) * ann_factor if down_std > 0 else 0.0
    wins     = tl[tl["win"]]["pnl_usd"]; losses = tl[~tl["win"]]["pnl_usd"]
    win_rate = tl["win"].mean()
    avg_win  = wins.mean() if len(wins) > 0 else 0.0
    avg_loss = losses.mean() if len(losses) > 0 else 0.0
    gross_profit = wins.sum(); gross_loss = abs(losses.sum())
    profit_factor = gross_profit / gross_loss if gross_loss > 0 else np.inf
    payoff_ratio  = avg_win / abs(avg_loss) if avg_loss < 0 else 0.0
    expectancy    = win_rate * avg_win + (1 - win_rate) * avg_loss
    win_arr = tl["win"].values.astype(int)
    cur_w = cur_l = max_w = max_l = 0
    for w_ in win_arr:
        if w_: cur_w += 1; cur_l = 0
        else:  cur_l += 1; cur_w = 0
        max_w = max(max_w, cur_w); max_l = max(max_l, cur_l)
    recovery_factor = net_pnl / abs(max_dd) if max_dd != 0 else np.nan
    ulcer_index     = float(np.sqrt(np.mean(dd_pct**2)))
    total_costs     = tl["cost_usd"].sum() if "cost_usd" in tl.columns else 0.0
    return {
        "starting_capital":   capital,    "final_equity":      final_equity,
        "net_pnl":            net_pnl,    "return_pct":        ret_pct,
        "cagr_pct":           cagr,       "max_drawdown":      max_dd,
        "max_drawdown_pct":   max_dd_pct, "calmar_ratio":      calmar,
        "recovery_factor":    recovery_factor, "ulcer_index":   ulcer_index,
        "sharpe_ratio":       sharpe,     "sortino_ratio":     sortino,
        "n_trades":           n,          "win_rate":          win_rate,
        "avg_win_usd":        avg_win,    "avg_loss_usd":      avg_loss,
        "expectancy_usd":     expectancy, "profit_factor":     profit_factor,
        "payoff_ratio":       payoff_ratio, "gross_profit":    gross_profit,
        "gross_loss":        -gross_loss, "total_costs_usd":   total_costs,
        "max_win_streak":     max_w,      "max_loss_streak":   max_l,
        "_equity":            equity,     "_dd_pct":           dd_pct,
        "_trade_log":         tl,
    }


