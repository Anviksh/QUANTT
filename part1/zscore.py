def extract_gnn_embeddings(model, graphs_scaled, dates_v, n_nodes):
    print("\n[Embedding Extraction] ...")
    model.eval()
    all_z, all_mse = [], []
    for bs in range(0, len(graphs_scaled), 64):
        batch = graphs_scaled[bs:bs+64]
        if PYGEOM_OK:
            bd   = graphs_to_batch_pygeom(batch).to(DEVICE)
            Xo   = torch.stack([torch.tensor(g[0],dtype=torch.float32).to(DEVICE) for g in batch])
            with torch.no_grad():
                z, recon, _ = model.forward_pygeom(bd)
        else:
            xl = [torch.tensor(g[0],dtype=torch.float32).to(DEVICE) for g in batch]
            al = [make_dense_adj(n_nodes,g[1],g[2],DEVICE) for g in batch]
            Xo = torch.stack(xl)
            with torch.no_grad():
                z, recon = model.forward_manual(xl, al)
        all_z.append(z.cpu().numpy())
        for b in range(len(batch)):
            all_mse.append(F.mse_loss(recon[b], Xo[b]).item())

    Z = np.vstack(all_z)
    GNN_LATENT_DF = pd.DataFrame(Z, index=dates_v,
                                  columns=[f"gnn_latent_{i+1}" for i in range(Z.shape[1])])
    RECON_MSE_SERIES = pd.Series(all_mse, index=dates_v, name="gnn_recon_mse")
    print(f"  Latent shape : {GNN_LATENT_DF.shape}")
    print(f"  MSE range    : [{RECON_MSE_SERIES.min():.6f}, {RECON_MSE_SERIES.max():.6f}]")
    return GNN_LATENT_DF, RECON_MSE_SERIES

def compute_graph_stats(graphs_scaled, dates_v, n_nodes, tickers_used=None):
    print("\n[Graph Stats] Computing topology statistics ...")
    if tickers_used is None: tickers_used = TRADING_UNIVERSE
    sector_idx = {i: SECTOR_MAP.get(tk,"Unknown") for i,tk in enumerate(tickers_used)}
    rows = []
    for t, (X, ei, ew) in enumerate(graphs_scaled):
        n_edges = ei.shape[1] if ei.shape[1] > 0 else 0
        density = n_edges / (n_nodes*(n_nodes-1)) if n_nodes > 1 else 0
        mean_ew = float(ew.mean()) if n_edges > 0 else 0.0
        std_ew  = float(ew.std())  if n_edges > 0 else 0.0
        degree  = n_edges / n_nodes if n_nodes > 0 else 0
        within, across = [], []
        if n_edges > 0:
            e = ei.numpy(); w = ew.numpy()
            for k in range(n_edges):
                i_, j_ = int(e[0,k]), int(e[1,k])
                si = sector_idx.get(i_,"?"); sj = sector_idx.get(j_,"?")
                (within if si==sj else across).append(w[k])
        rows.append({
            "mean_edge_weight": mean_ew, "edge_density": density,
            "avg_degree": degree, "edge_weight_std": std_ew,
            "within_sector_corr": np.mean(within) if within else 0.0,
            "cross_sector_corr":  np.mean(across) if across else 0.0,
            "sector_corr_ratio":  (np.mean(within)/(np.mean(across)+1e-8)
                                    if within and across else 1.0),
        })
    return pd.DataFrame(rows, index=dates_v)

def cluster_gnn_regimes(GNN_LATENT_DF, vix_series, train_end=TRAIN_END):
    print("\n[Clustering] KMeans on graph embeddings ...")
    train_mask = GNN_LATENT_DF.index <= pd.Timestamp(train_end, tz="UTC")
    Z_train    = GNN_LATENT_DF.loc[train_mask].values
    Z_all      = GNN_LATENT_DF.values
    print("  Silhouette scores:")
    for k in [3,4,5,6,7]:
        km_ = KMeans(n_clusters=k, n_init=10, random_state=42)
        labs= km_.fit_predict(Z_train)
        if len(np.unique(labs)) > 1:
            sil = silhouette_score(Z_train, labs, sample_size=min(2000, len(Z_train)))
            print(f"    k={k}: silhouette={sil:.4f}")
    kmeans  = KMeans(n_clusters=N_REGIMES, n_init=20, random_state=42)
    kmeans.fit(Z_train)
    vix_al    = vix_series.reindex(GNN_LATENT_DF.index, method="ffill").bfill()
    vix_train = vix_al.loc[train_mask].values
    raw_labels= kmeans.labels_
    cluster_vix = {k: vix_train[raw_labels==k].mean()
                   for k in range(N_REGIMES) if (raw_labels==k).sum() > 0}
    sorted_by_vix = sorted(cluster_vix, key=lambda k: cluster_vix[k])
    remap = {raw: new for new, raw in enumerate(sorted_by_vix)}
    print(f"  VIX-sorted remap: {remap}")
    raw_all    = kmeans.predict(Z_all)
    new_labels = np.array([remap[r] for r in raw_all])
    new_centroids = np.zeros_like(kmeans.cluster_centers_)
    for raw, new in remap.items():
        new_centroids[new] = kmeans.cluster_centers_[raw]
    dists    = pairwise_distances(Z_all, new_centroids)
    sorted_d = np.sort(dists, axis=1)
    d1, d2   = sorted_d[:,0], sorted_d[:,1]
    confidence = np.clip(1.0 - np.where(d2>0, d1/d2, 0.0), 0, 1)
    GNN_REGIME_SERIES = pd.Series(new_labels, index=GNN_LATENT_DF.index, name="gnn_regime")
    GNN_REGIME_CONF   = pd.Series(confidence, index=GNN_LATENT_DF.index, name="gnn_conf")
    print(f"\n  Regime distribution:\n  {GNN_REGIME_SERIES.value_counts().sort_index()}")
    return GNN_REGIME_SERIES, GNN_REGIME_CONF, kmeans, remap, new_centroids


