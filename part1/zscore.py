# CELL 20 — Walk-Forward Loop: all 3 folds
# ═══════════════════════════════════════════════════════════════════════════════

def run_walkforward_all_folds():
    print("\n" + "╔"+"═"*66+"╗")
    print("║  WALK-FORWARD XGBoost + HAWKES  —  3 FOLDS                   ║")
    print("╚"+"═"*66+"╝")

    fold_results    = {}
    all_oos_trades  = []

    for fold_cfg in FOLDS:
        fold_id    = fold_cfg["fold"]
        f_tr_start = fold_cfg["train_start"]
        f_tr_end   = fold_cfg["train_end"]
        f_oos_s    = fold_cfg["oos_start"]
        f_oos_e    = fold_cfg["oos_end"]

        print(f"\n{'─'*68}")
        print(f"  FOLD {fold_id}:  Train {f_tr_start}→{f_tr_end}  |  OOS {f_oos_s}→{f_oos_e}")
        print(f"{'─'*68}")

        # ── 1. Build feature matrix ──────────────────────────────────────────
        # For folds 2/3, if we can reuse (same configs but different date slice),
        # we rebuild fresh to ensure correct Hawkes state
        train_df, oos_df = build_feature_matrix_from_all_configs(
            zscore_full_df,
            all_raw_trades_by_config,
            fold_train_start=f_tr_start,
            fold_train_end=f_tr_end,
            fold_oos_start=f_oos_s,
            fold_oos_end=f_oos_e,
        )

        if train_df.empty:
            print(f"  ⚠  Empty training data for fold {fold_id} — skipping")
            continue

        # ── 2. Fit Hawkes decay parameters on training data ──────────────────
        # Use the training trade log to fit per-regime Hawkes parameters
        ref_ticker = "SPY" if "SPY" in stock_data else list(stock_data.keys())[0]
        ref_bar_ts = stock_data[ref_ticker].index

        train_trades_for_hawkes = (
            train_df[["entry_ts","ticker","direction","gnn_regime_raw"]]
            .rename(columns={"gnn_regime_raw":"gnn_regime"})
        )
        hawkes_params = fit_hawkes_per_regime(
            train_trades_for_hawkes, ref_bar_ts, GNN_REGIME)

        # Save Hawkes params
        hp_path = os.path.join(XGB_OUT_DIR, "models",
                               f"hawkes_params_fold_{fold_id}.json")
        with open(hp_path, "w") as f:
            json.dump({str(k): v for k,v in hawkes_params.items()}, f, indent=2)

        # Compute per-regime thresholds
        hawkes_thresholds = compute_regime_hawkes_thresholds(
            ref_bar_ts, GNN_REGIME, hawkes_params)
        print(f"\n  Hawkes thresholds: { {r: f'{v:.4f}' for r,v in hawkes_thresholds.items()} }")

        # ── 3. Train XGBoost models ──────────────────────────────────────────
        models, scalers = train_regime_xgb_models(train_df, fold_id)
        plot_feature_importance(models, fold_id)

        # ── 4. OOS inference ─────────────────────────────────────────────────
        oos_selected, oos_meta = run_oos_inference(
            oos_df, models, scalers, hawkes_params, hawkes_thresholds, fold_id)

        # ── 5. Evaluate fold ─────────────────────────────────────────────────
        if not oos_selected.empty and "pnl_usd" in oos_selected.columns:
            bt_yrs = max(
                (pd.to_datetime(oos_selected["exit_ts"]).max() -
                 pd.to_datetime(oos_selected["exit_ts"]).min()).days / 365.25,
                0.1)
            pm = compute_portfolio_metrics(oos_selected, STARTING_CAPITAL, bt_yrs)
        else:
            pm = {"n_trades":0,"sharpe_ratio":0,"win_rate":0,
                  "net_pnl":0,"max_drawdown_pct":0,"calmar_ratio":0}

        # Per-regime breakdown
        regime_stats = {}
        if not oos_selected.empty:
            for r in range(N_REGIMES):
                r_sub = oos_selected[oos_selected["gnn_regime_raw"] == r]
                if r_sub.empty:
                    regime_stats[r] = {"n":0,"wr":0.0,"pnl":0.0}
                    continue
                regime_stats[r] = {
                    "n":   len(r_sub),
                    "wr":  float(r_sub["win"].mean()),
                    "pnl": float(r_sub["pnl_usd"].sum()) if "pnl_usd" in r_sub else 0.0,
                }

        fold_results[fold_id] = {
            "sharpe":     pm.get("sharpe_ratio", 0),
            "calmar":     pm.get("calmar_ratio", 0),
            "max_dd":     pm.get("max_drawdown_pct", 0),
            "win_rate":   pm.get("win_rate", 0),
            "net_pnl":    pm.get("net_pnl", 0),
            "n_trades":   pm.get("n_trades", 0),
            "hawkes_filter_pct": oos_meta.get("hawkes_filtered_pct", 0),
            "regime_stats": regime_stats,
            "pm": pm,
        }

        print(f"\n  ╔─ Fold {fold_id} OOS Results ─────────────────────────────────")
        print(f"  ║  Trades     : {pm.get('n_trades',0):,}")
        print(f"  ║  Sharpe     : {pm.get('sharpe_ratio',0):.3f}")
        print(f"  ║  Calmar     : {pm.get('calmar_ratio',0):.3f}")
        print(f"  ║  Max DD     : {pm.get('max_drawdown_pct',0):.2f}%")
        print(f"  ║  Win Rate   : {pm.get('win_rate',0)*100:.1f}%")
        print(f"  ║  Net P&L    : ${pm.get('net_pnl',0):+,.0f}")
        print(f"  ║  Hawkes %   : {oos_meta.get('hawkes_filtered_pct',0):.1f}% signals filtered")
        print(f"  ╚──────────────────────────────────────────────────────────────")

        # Save OOS trades
        if not oos_selected.empty:
            trade_cols = [c for c in oos_selected.columns
                          if c not in ["entry_day"]]
            oos_selected[trade_cols].to_csv(
                os.path.join(XGB_OUT_DIR, "backtest",
                             f"fold_{fold_id}_oos_trades.csv"), index=False)
            all_oos_trades.append(oos_selected[trade_cols])

    # Combine all OOS folds
    if all_oos_trades:
        combined_oos = pd.concat(all_oos_trades, ignore_index=True)
        combined_oos.to_csv(
            os.path.join(XGB_OUT_DIR, "backtest", "all_folds_oos_trades.csv"),
            index=False)
        print(f"\n✅ Combined OOS trades: {len(combined_oos):,} total  [{elapsed()}]")
    else:
        combined_oos = pd.DataFrame()

    return fold_results, combined_oos


