def build_portfolio_trades(top_cfg, window="train"):
    all_pt = []
    for _, cfg_row in top_cfg.iterrows():
        cid = cfg_row["config_id"]
        if cid not in all_raw_trades_by_config_FULL: continue
        src = all_raw_trades_by_config_FULL[cid]
        trades = simulate_trades(
            src["arr_open"],
            src["arr_high"], src["arr_low"], src["arr_close"],
            src["arr_z"],    src["arr_atr"], src["arr_val"],
            src["arr_vah"],  src["c_scores"],
            src["z_lo"],     src["z_hi"],
            src["direction"],src["mh"],      src["bar_ts"])
        for t in trades:
            if window == "train" and t["exit_ts"] > TRAIN_END:  continue
            if window == "test"  and t["exit_ts"] < TEST_START: continue
            sl_pts = t["sl_pts"]
            if sl_pts <= 0: continue
            position_size = MAX_RISK_PER_TRADE / sl_pts
            all_pt.append({
                "config_id":      cid,
                "ticker":         cfg_row["ticker"],
                "direction":      t["direction"],
                "entry_ts":       t["entry_ts"],
                "exit_ts":        t["exit_ts"],
                "entry_price":    t["entry_price"],
                "exit_price":     t["exit_price"],
                "pnl_pts":        t["pnl_pts"],
                "sl_pts":         sl_pts,
                "position_size":  position_size,
                "risk_usd":       MAX_RISK_PER_TRADE,
                "pnl_usd":        t["pnl_pts"] * position_size,
                "cost_usd":       t["cost_rt"]  * position_size,
                "win":            t["win"],
                "outcome":        t["outcome"],
                "is_overnight":   t["is_overnight"],
                "hold_bars":      t["hold_bars"],
                "zone":           cfg_row["zone"],
                "z_formula":      cfg_row["z_formula"],
                "window_days":    cfg_row["window_days"],
            })
    return pd.DataFrame(all_pt)

