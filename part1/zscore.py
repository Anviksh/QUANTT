def make_xgb_groups(df):
    """
    XGBRanker requires 'group' = number of items per query.
    Query = (date × asset). We group by entry_day + ticker.
    """
    df = df.copy()
    df["entry_day"] = pd.to_datetime(df["entry_ts"]).dt.normalize()
    group_counts = (df.groupby(["entry_day","ticker"])
                      .size()
                      .reset_index(name="cnt"))
    counts = group_counts["cnt"].tolist()
    # Sort df by the same keys so groups align
    df = df.sort_values(["entry_day","ticker"]).reset_index(drop=True)
    return df, counts


def clip_and_scale_features(train_df, feature_cols=FEATURE_COLS):
    """Clip infinite values and fit a RobustScaler on train data."""
    X = train_df[feature_cols].copy()
    # Replace inf with large finite values
    X = X.replace([np.inf, -np.inf], np.nan)
    X = X.fillna(X.median())
    X = X.clip(-10, 10)
    scaler = RobustScaler()
    X_sc   = pd.DataFrame(scaler.fit_transform(X), columns=feature_cols, index=X.index)
    return X_sc, scaler


