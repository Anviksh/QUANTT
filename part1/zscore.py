# ── Cell 3 : Alpaca Data Manager ──────────────────────────────

def _cache_path(ticker: str) -> str:
    return os.path.join(CACHE_DIR, f"{ticker}_1h.parquet")

def _load_meta() -> dict:
    if os.path.exists(CACHE_META):
        with open(CACHE_META, "r") as f:
            return json.load(f)
    return {}

def _save_meta(meta: dict):
    with open(CACHE_META, "w") as f:
        json.dump(meta, f, indent=2, default=str)

def _fetch_bars(client: StockHistoricalDataClient,
                ticker: str,
                start: datetime,
                end: datetime) -> pd.DataFrame:
    try:
        req = StockBarsRequest(
            symbol_or_symbols=[ticker],
            timeframe=TimeFrame.Hour,
            start=start,
            end=end,
            feed=DATA_FEED,
            adjustment="all",
        )
        bars = client.get_stock_bars(req)
        df   = bars.df
        if df is None or df.empty:
            return pd.DataFrame()
        if isinstance(df.index, pd.MultiIndex):
            df = df.xs(ticker, level="symbol")
        df.index = pd.to_datetime(df.index, utc=True)
        df = df[["open","high","low","close","volume"]].sort_index()
        return df
    except Exception as e:
        print(f"\n    ⚠  Alpaca fetch error [{ticker}]: {e}")
        return pd.DataFrame()

def load_or_fetch(client: StockHistoricalDataClient,
                  ticker: str,
                  force_refresh: bool = False) -> pd.DataFrame:
    cache_file = _cache_path(ticker)
    meta       = _load_meta()
    now_utc    = datetime.now(UTC)

    if os.path.exists(cache_file) and not force_refresh:
        try:
            df_cached = pd.read_parquet(cache_file)
            df_cached.index = pd.to_datetime(df_cached.index, utc=True)
        except Exception:
            df_cached = pd.DataFrame()

        if not df_cached.empty:
            last_bar = df_cached.index.max()
            if (now_utc - last_bar).total_seconds() < 7200:
                return df_cached
            fetch_from = last_bar + timedelta(hours=1)
            df_new = _fetch_bars(client, ticker, fetch_from, now_utc)
            if not df_new.empty:
                df_combined = pd.concat([df_cached, df_new])
                df_combined = (df_combined[~df_combined.index.duplicated(keep="last")]
                               .sort_index())
            else:
                df_combined = df_cached
            df_combined.to_parquet(cache_file)
            meta[ticker] = {"last_updated": now_utc.isoformat(),
                            "n_bars": len(df_combined)}
            _save_meta(meta)
            return df_combined

    df = _fetch_bars(client, ticker, FETCH_START, now_utc)
    if df.empty:
        return pd.DataFrame()
    df.to_parquet(cache_file)
    meta[ticker] = {"last_updated": now_utc.isoformat(), "n_bars": len(df)}
    _save_meta(meta)
    return df


# ── Cell 4 : Data Quality Checks ──────────────────────────────

def check_and_clean(df: pd.DataFrame, ticker: str,
                    min_bars: int = 500) -> Tuple[pd.DataFrame, dict]:
    report = {"ticker": ticker, "issues": [], "passed": True}
    n0 = len(df)

    if n0 < min_bars:
        report["issues"].append(f"Insufficient bars: {n0} < {min_bars}")
        report["passed"] = False
        return df, report

    dup = df.index.duplicated(keep="last").sum()
    if dup > 0:
        df = df[~df.index.duplicated(keep="last")]
        report["issues"].append(f"Removed {dup} duplicate timestamps")

    df = df.sort_index()

    bad_px = (df[["open","high","low","close"]] <= 0).any(axis=1)
    if bad_px.sum() > 0:
        df = df[~bad_px]
        report["issues"].append(f"Removed {bad_px.sum()} zero/negative price bars")

    bad_ohlc = (
        (df["high"] < df["low"])   |
        (df["high"] < df["open"])  |
        (df["high"] < df["close"]) |
        (df["low"]  > df["open"])  |
        (df["low"]  > df["close"])
    )
    if bad_ohlc.sum() > 0:
        df = df[~bad_ohlc]
        report["issues"].append(f"Removed {bad_ohlc.sum()} OHLC-inconsistent bars")

    zero_vol = (df["volume"] <= 0).sum()
    if zero_vol > 0:
        df["volume"] = (df["volume"].replace(0, np.nan)
                        .ffill().bfill().fillna(1.0))
        report["issues"].append(f"Fixed {zero_vol} zero-volume bars (ffill)")

    med_vol = df["volume"].median()
    cap     = med_vol * 100
    spikes  = (df["volume"] > cap).sum()
    if spikes > 0:
        df.loc[df["volume"] > cap, "volume"] = cap
        report["issues"].append(f"Capped {spikes} volume spikes at 100× median")

    try:
        et_days  = set(df.index.tz_convert(EASTERN).normalize().date)
        biz_days = pd.bdate_range(df.index[0].date(), df.index[-1].date())
        streak = 0; long_gaps = 0
        for d in biz_days:
            if d.date() not in et_days:
                streak += 1
                if streak > 3: long_gaps += 1
            else:
                streak = 0
        if long_gaps > 5:
            report["issues"].append(
                f"⚠ {long_gaps} multi-day weekday data gaps (data may be sparse early on)")
    except Exception:
        pass

    if len(df) < min_bars:
        report["passed"] = False
        report["issues"].append(
            f"After cleaning: only {len(df)} bars remain < {min_bars}")

    report["bars_original"] = n0
    report["bars_clean"]    = len(df)
    report["n_issues"]      = len(report["issues"])
    return df, report


