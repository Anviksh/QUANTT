# CELL 14 — Hawkes Process
# ═══════════════════════════════════════════════════════════════════════════════

class HawkesProcess:
    """λ(t) = μ + Σ κ × exp(−δ × (t − tᵢ))"""
    def __init__(self, mu=0.05, kappa=0.2, delta=0.1):
        self.mu = mu; self.kappa = kappa; self.delta = delta
        self.events: List[float] = []

    def intensity(self, t: float) -> float:
        if not self.events: return self.mu
        dt   = np.array([t - ti for ti in self.events if ti <= t])
        if len(dt) == 0: return self.mu
        return float(self.mu + self.kappa * np.sum(np.exp(-self.delta * dt)))

    def update(self, t: float):
        self.events.append(t)
        # Prune old events (keep last 200 for efficiency)
        if len(self.events) > 200:
            self.events = self.events[-200:]

    def reset(self):
        self.events = []


def fit_hawkes_per_regime(trade_df, bar_ts_series, regime_series):
    """
    Fit Hawkes decay parameters (μ, κ, δ) per regime using maximum likelihood.
    """
    print("\n[Hawkes Fitting]")
    if trade_df is None or trade_df.empty:
        return {r: {"mu":0.05,"kappa":0.2,"delta":HAWKES_DECAY_INIT[r]/BARS_PER_DAY}
                for r in range(5)}

    bar_ts_arr = pd.DatetimeIndex(bar_ts_series)
    bar_to_idx = {ts: i for i, ts in enumerate(bar_ts_arr)}
    regime_at_bar = {}
    for ts in bar_ts_arr:
        d = ts.normalize()
        if GNN_REGIME is not None:
            idx_ = GNN_REGIME.index.get_indexer([d], method="nearest")[0]
            r = int(GNN_REGIME.iloc[idx_]) if idx_>=0 else 2
        else:
            r = 2
        regime_at_bar[ts] = r

    params = {}
    for r in range(5):
        regime_bars = [ts for ts, reg in regime_at_bar.items() if reg==r]
        trade_ts    = pd.to_datetime(trade_df["entry_ts"]) if "entry_ts" in trade_df.columns else pd.Series()
        regime_trade_bars = []
        for ts in trade_ts:
            ts_utc = ts.tz_localize("UTC") if ts.tzinfo is None else ts
            nearest = bar_ts_arr[bar_ts_arr.get_indexer([ts_utc], method="nearest")[0]]
            if regime_at_bar.get(nearest, -1) == r:
                idx_ = bar_to_idx.get(nearest, None)
                if idx_ is not None: regime_trade_bars.append(float(idx_))

        if len(regime_trade_bars) < 5:
            default_delta = HAWKES_DECAY_INIT[r] / BARS_PER_DAY
            params[r] = {"mu":0.05,"kappa":0.2,"delta":default_delta}
            print(f"  Regime {r}: insufficient events ({len(regime_trade_bars)}) "
                  f"— using default δ={default_delta:.3f}")
            continue

        T_total = len(regime_bars)
        events  = sorted(regime_trade_bars)

        def neg_loglik(theta):
            mu_, kappa_, delta_ = max(theta[0],1e-6), max(theta[1],1e-6), max(theta[2],1e-6)
            ll = 0.0
            for i, ti in enumerate(events):
                lam = mu_
                for tj in events[:i]:
                    if ti-tj < 50: lam += kappa_*np.exp(-delta_*(ti-tj))
                ll += np.log(max(lam,1e-9))
            ll -= mu_*T_total
            if delta_ > 0:
                for tj in events:
                    ll -= (kappa_/delta_)*(1-np.exp(-delta_*(T_total-tj)))
            return -ll

        default_delta = HAWKES_DECAY_INIT[r] / BARS_PER_DAY
        try:
            res = minimize(neg_loglik, [0.05, 0.2, default_delta],
                           method="Nelder-Mead",
                           options={"maxiter":1000,"xatol":1e-4,"fatol":1e-4})
            mu_, kappa_, delta_ = [max(v,1e-6) for v in res.x]
        except Exception:
            mu_, kappa_, delta_ = 0.05, 0.2, default_delta

        params[r] = {"mu":mu_,"kappa":kappa_,"delta":delta_}
        print(f"  Regime {r}: n_events={len(events)}  μ={mu_:.4f}  "
              f"κ={kappa_:.4f}  δ={delta_:.4f} "
              f"(half-life≈{np.log(2)/delta_:.1f} bars)")

    return params


def compute_regime_hawkes_thresholds(bar_ts_series, regime_series, hawkes_params):
    """Compute 75th percentile Hawkes intensity threshold per regime."""
    bar_ts_arr = pd.DatetimeIndex(bar_ts_series)
    thresholds = {}
    for r in range(5):
        p = hawkes_params.get(r, {"mu":0.05,"kappa":0.2,"delta":0.1})
        proc = HawkesProcess(mu=p["mu"], kappa=p["kappa"], delta=p["delta"])
        intensities = []
        mock_event_interval = 10
        for i, ts in enumerate(bar_ts_arr):
            d = ts.normalize()
            if GNN_REGIME is not None:
                idx_ = GNN_REGIME.index.get_indexer([d],method="nearest")[0]
                bar_r = int(GNN_REGIME.iloc[idx_]) if idx_>=0 else 2
            else:
                bar_r = 2
            if bar_r == r:
                intensities.append(proc.intensity(float(i)))
            if i % mock_event_interval == 0:
                proc.update(float(i))
        if intensities:
            thresholds[r] = float(np.percentile(intensities, HAWKES_THRESHOLD_PERCENTILE))
        else:
            thresholds[r] = 1.0
    return thresholds


