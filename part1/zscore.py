# ── Manual GCN (fallback — no PyG needed) ─────────────────────────────────────
class ManualGCNLayer(nn.Module):
    def __init__(self, in_dim, out_dim):
        super().__init__()
        self.linear = nn.Linear(in_dim, out_dim, bias=False)
        self.bn     = nn.BatchNorm1d(out_dim)
    def forward(self, x, adj):
        h = self.linear(x)
        h = torch.mm(adj, h)
        h = self.bn(h)
        return F.relu(h)

class ManualGCNEncoder(nn.Module):
    def __init__(self, n_node_feat, hidden=GNN_HIDDEN, latent=LATENT_DIM, n_layers=N_GNN_LAYERS):
        super().__init__()
        self.layers = nn.ModuleList()
        dims = [n_node_feat] + [hidden]*(n_layers-1) + [latent]
        for i in range(n_layers):
            self.layers.append(ManualGCNLayer(dims[i], dims[i+1]))
    def normalise_adj(self, adj):
        N = adj.shape[0]
        A = adj + torch.eye(N, device=adj.device)
        deg = A.sum(dim=1)
        D_inv_sqrt = torch.diag(deg.pow(-0.5).clamp(max=1e4))
        return D_inv_sqrt @ A @ D_inv_sqrt
    def forward(self, x, adj):
        A_norm = self.normalise_adj(adj)
        h = x
        for layer in self.layers: h = layer(h, A_norm)
        return h.mean(dim=0), h

# ── GAT Encoder (PyG) ─────────────────────────────────────────────────────────
if PYGEOM_OK:
    class GATEncoder(nn.Module):
        def __init__(self, n_node_feat, hidden=GNN_HIDDEN, latent=LATENT_DIM,
                     heads=GNN_HEADS, n_layers=N_GNN_LAYERS):
            super().__init__()
            self.convs = nn.ModuleList(); self.bns = nn.ModuleList()
            in_dim = n_node_feat
            for i in range(n_layers):
                out_dim = hidden//heads if i < n_layers-1 else latent//heads
                self.convs.append(GATConv(in_dim, out_dim, heads=heads,
                    edge_dim=1, concat=True, dropout=0.2))
                self.bns.append(nn.BatchNorm1d(out_dim*heads))
                in_dim = out_dim*heads
            self.fc_final = nn.Linear(in_dim*2, latent)
        def forward(self, data):
            x, ei, ea, batch = data.x, data.edge_index, data.edge_attr, data.batch
            if ea is not None and ea.dim() == 1: ea = ea.unsqueeze(-1)
            h = x
            for conv, bn in zip(self.convs, self.bns):
                h = conv(h, ei, edge_attr=ea)
                h = bn(h); h = F.elu(h)
                h = F.dropout(h, p=0.2, training=self.training)
            g = torch.cat([global_mean_pool(h,batch), global_max_pool(h,batch)], dim=-1)
            return self.fc_final(g), h

# ── Decoder ───────────────────────────────────────────────────────────────────
class GraphDecoder(nn.Module):
    def __init__(self, latent_dim, n_nodes, n_node_feat, hidden=GNN_HIDDEN):
        super().__init__()
        self.n_nodes     = n_nodes
        self.n_node_feat = n_node_feat
        self.net = nn.Sequential(
            nn.Linear(latent_dim, hidden), nn.BatchNorm1d(hidden), nn.ReLU(), nn.Dropout(0.2),
            nn.Linear(hidden, hidden*2), nn.BatchNorm1d(hidden*2), nn.ReLU(),
            nn.Linear(hidden*2, n_nodes*n_node_feat),
        )
    def forward(self, z):
        single = z.dim() == 1
        if single: z = z.unsqueeze(0)
        out = self.net(z).view(-1, self.n_nodes, self.n_node_feat)
        if single: out = out.squeeze(0)
        return out

# ── Full GNN Autoencoder ──────────────────────────────────────────────────────
class MarketGNN(nn.Module):
    def __init__(self, n_node_feat, n_nodes, hidden=GNN_HIDDEN, latent=LATENT_DIM,
                 heads=GNN_HEADS, n_layers=N_GNN_LAYERS):
        super().__init__()
        self.use_pygeom = PYGEOM_OK
        self.n_nodes    = n_nodes
        self.latent_dim = latent
        if PYGEOM_OK:
            self.encoder = GATEncoder(n_node_feat, hidden, latent, heads, n_layers)
        else:
            self.encoder = ManualGCNEncoder(n_node_feat, hidden, latent, n_layers)
        self.decoder = GraphDecoder(latent, n_nodes, n_node_feat, hidden)

    def forward_pygeom(self, data):
        z, h = self.encoder(data)
        return z, self.decoder(z), h

    def forward_manual(self, x_batch, adj_batch):
        zs, recons = [], []
        for x, adj in zip(x_batch, adj_batch):
            z, _ = self.encoder(x, adj)
            zs.append(z); recons.append(self.decoder(z))
        return torch.stack(zs), torch.stack(recons)

def make_dense_adj(n_nodes, edge_index, edge_weight, device):
    adj = torch.zeros(n_nodes, n_nodes, dtype=torch.float32, device=device)
    if edge_index.shape[1] > 0:
        adj[edge_index.to(device)[0], edge_index.to(device)[1]] = edge_weight.to(device)
    return adj

def graphs_to_batch_pygeom(graphs_subset):
    data_list = []
    for X, ei, ew in graphs_subset:
        x  = torch.tensor(X, dtype=torch.float32)
        ea = ew.unsqueeze(-1) if ew.dim()==1 else ew
        data_list.append(Data(x=x, edge_index=ei, edge_attr=ea))
    return Batch.from_data_list(data_list)


