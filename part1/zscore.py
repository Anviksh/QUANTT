def build_return_matrix(daily_dict, tickers=None, log_returns=True):
    if tickers is None: tickers = sorted(daily_dict.keys())
    tickers = [t for t in tickers if t in daily_dict]
    close_frames = {t: daily_dict[t]["close"] for t in tickers}
    price_df = pd.DataFrame(close_frames).sort_index().ffill()
    ret_df   = np.log(price_df / price_df.shift(1)) if log_returns else price_df.pct_change()
    ret_df   = ret_df.dropna(how="all")
    print(f"[Return Matrix] Shape: {ret_df.shape}  "
          f"({ret_df.index[0].date()} → {ret_df.index[-1].date()})")
    return ret_df, price_df

def build_node_features(ret_df, price_df, daily_dict, w=NODE_FEAT_WINDOW):
    print("\n[Node Features] Computing ...")
    tickers = ret_df.columns.tolist()
    N, dates, T = len(tickers), ret_df.index, len(ret_df)
    feat = np.zeros((T, N, 10), dtype=np.float32)
    for i, tk in enumerate(tickers):
        if tk not in price_df.columns: continue
        c, r = price_df[tk], ret_df[tk]
        r5   = r.rolling(5).sum()
        r20  = r.rolling(20).sum()
        v5   = r.rolling(5).std()  * np.sqrt(252)
        v20  = r.rolling(20).std() * np.sqrt(252)
        ma20 = c.rolling(20).mean()
        sd20 = c.rolling(20).std()
        z    = (c - ma20) / sd20.replace(0, np.nan)
        ma50 = c.rolling(50).mean()
        above_ma50 = np.sign(c - ma50)
        delta = c.diff()
        gain  = delta.clip(lower=0).ewm(com=13, adjust=False).mean()
        loss  = (-delta.clip(upper=0)).ewm(com=13, adjust=False).mean()
        rs    = gain / loss.replace(0, np.nan)
        rsi_n = ((100 - 100/(1+rs)) - 50) / 50
        if tk in daily_dict:
            high   = daily_dict[tk]["high"]
            low_   = daily_dict[tk]["low"]
        else:
            high = low_ = c
        prev_c = c.shift(1)
        tr_s   = pd.concat([(high-low_),(high-prev_c).abs(),(low_-prev_c).abs()],axis=1).max(axis=1)
        atr14  = tr_s.ewm(alpha=1/14, adjust=False).mean()
        atr_pct= atr14 / c.replace(0, np.nan)
        def align(s): return s.reindex(dates).ffill().bfill().fillna(0).values.astype(np.float32)
        feat[:,i,0]=align(r);       feat[:,i,1]=align(r5)
        feat[:,i,2]=align(r20);     feat[:,i,3]=align(v5)
        feat[:,i,4]=align(v20);     feat[:,i,5]=align(v5/v20.replace(0,np.nan))
        feat[:,i,6]=align(z);       feat[:,i,7]=align(rsi_n)
        feat[:,i,8]=align(above_ma50); feat[:,i,9]=align(atr_pct)
    feat = np.clip(feat, -5, 5)
    feat = np.where(np.isfinite(feat), feat, 0.0)
    print(f"  Node feature tensor: {feat.shape}")
    return feat, tickers, dates

def build_daily_graphs(ret_df, node_feat, dates,
                       corr_window=CORR_WINDOW, top_k=TOP_K_EDGES,
                       edge_threshold=EDGE_THRESHOLD, use_topk=USE_TOPK):
    print(f"\n[Graph Construction] window={corr_window}d  top-{top_k}")
    rets = ret_df.values.astype(np.float32)
    T, N = rets.shape
    graphs = []
    for t in range(T):
        X = node_feat[t]
        start_t = max(0, t - corr_window)
        window  = rets[start_t:t]
        if window.shape[0] < 5:
            graphs.append((X, torch.zeros((2,0),dtype=torch.long),
                           torch.zeros(0,dtype=torch.float32))); continue
        W     = window - window.mean(axis=0, keepdims=True)
        norms = np.linalg.norm(W, axis=0, keepdims=True) + 1e-8
        corr  = (W/norms).T @ (W/norms) / window.shape[0]
        np.fill_diagonal(corr, 0.0)
        abs_corr = np.abs(corr)
        if use_topk:
            rows, cols, weights = [], [], []
            for i in range(N):
                top_j = np.argsort(abs_corr[i])[::-1][:top_k]
                for j in top_j:
                    if abs_corr[i,j] > 0.05:
                        rows.append(i); cols.append(j); weights.append(float(abs_corr[i,j]))
        else:
            mask = abs_corr >= edge_threshold
            rows, cols = np.where(mask)
            weights = abs_corr[mask].tolist()
            rows, cols = rows.tolist(), cols.tolist()
        if len(rows) == 0:
            graphs.append((X, torch.zeros((2,0),dtype=torch.long),
                           torch.zeros(0,dtype=torch.float32)))
        else:
            graphs.append((X, torch.tensor([rows,cols],dtype=torch.long),
                           torch.tensor(weights,dtype=torch.float32)))
    avg_e = np.mean([g[1].shape[1] for g in graphs if g[1].shape[1]>0])
    print(f"  Built {len(graphs)} graphs  |  avg {avg_e:.0f} edges/day")
    return graphs

